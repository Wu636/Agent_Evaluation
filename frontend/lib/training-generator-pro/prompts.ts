/**
 * 构建 Pro 版剧本生成 Prompt
 */
export function buildProScriptGeneratorPrompt(
  teacherDoc: string,
  template?: string,
  userGenerationAdvice?: string,
): string {
  const prompt = (template || PRO_SCRIPT_TEMPLATE)
    .split("{teacherDoc}")
    .join(teacherDoc);
  const advice = String(userGenerationAdvice || "").trim();
  if (!advice) return prompt;

  return `${prompt}

# 用户生成配置建议（选填补充约束）

请在不违背教师任务文档事实、不破坏上述输出格式和必填规则的前提下，同时遵循以下用户建议。如建议与教师文档明确事实冲突，以教师文档为准；其他情况下尽量落实建议。

<user_generation_advice>
${advice.slice(0, 4000)}
</user_generation_advice>`;
}
